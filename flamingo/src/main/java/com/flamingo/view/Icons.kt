@DsIconSet
internal val Int.dsIcon: Int
    get() = requireDsIcon(this)

@DsIconSet
public fun requireDsIcon(@DrawableRes iconRes: Int): Int {
    require(designSystemIcons.contains(iconRes)) {
        "Icon $iconRes is not part of the design system icon set"
    }
    return iconRes
}

